function out =  hexStr2uint8M(H) 
%take the 128-bit hex string and convert it into a 4x4 uint8 matrix 
    out = [ uint8(hex2dec(H(1:2))), uint8(hex2dec(H(9:10))) , uint8(hex2dec(H(17:18))), uint8(hex2dec(H(25:26))); ...
            uint8(hex2dec(H(3:4))), uint8(hex2dec(H(11:12))), uint8(hex2dec(H(19:20))), uint8(hex2dec(H(27:28))); ...
            uint8(hex2dec(H(5:6))), uint8(hex2dec(H(13:14))), uint8(hex2dec(H(21:22))), uint8(hex2dec(H(29:30))); ...
            uint8(hex2dec(H(7:8))), uint8(hex2dec(H(15:16))), uint8(hex2dec(H(23:24))), uint8(hex2dec(H(31:32)))];
end